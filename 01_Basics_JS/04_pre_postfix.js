// Prefix & Post Fix
let x = 5;
let prefix = ++x;
console.log(x, prefix);             // 6 6  return value after incrementing

let x1 = 5;
let prefix1 = x1++;
console.log(x1, prefix1);           //6 5   increment value incement and return value before increment 

let x2 = 5;
let prefix2 = --x2;
console.log(x2, prefix2);          // 4 4

let x3 = 5;
let prefix3 = x3--;
console.log(x3, prefix3);          // 4 5